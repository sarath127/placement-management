    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed
       try {
                 Connection con = Connect.ConnectDB();
                 String value1=c_type.getSelectedItem().toString();
                 
                PreparedStatement ps = con.prepareStatement("INSERT INTO company(c_name,seats,c_type)"
                        + "values(?,?,?) ");
                
                ps.setString(1, c_name.getText());
                ps.setString(2, seats.getText());
                 ps.setString(3, value1);
                ps.executeUpdate();
                update_table();
               
                
                JOptionPane.showMessageDialog(null, "Data Inserted");
            } catch (SQLException | HeadlessException ex) {
                 JOptionPane.showMessageDialog(null, ex.getMessage());
            } 
    }//GEN-LAST:event_insertActionPerformed

    private void company_tableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_company_tableKeyPressed
        if((evt.getKeyCode() == KeyEvent.VK_UP) || (evt.getKeyCode() == KeyEvent.VK_DOWN))
        {
         try{
            int row =company_table.getSelectedRow();
            String t=(company_table.getModel().getValueAt(row,0).toString());
            String s="select * from company where c_id="+t ;
            Connection con = Connect.ConnectDB();
            pst = con.prepareStatement(s);
            rst=pst.executeQuery();
            if(rst.next())
            {
                String a=rst.getString("c_id");
                c_id.setText(a);
                String b=rst.getString("c_name");
                c_name.setText(b);
                String c=rst.getString("seats");
                seats.setText(c);
                String d=rst.getString("c_type");
                c_type.setSelectedItem(d);
           
                
            }
        }
        catch(Exception ex)
        {
            
        }   
        }
    }//GEN-LAST:event_company_tableKeyPressed

