    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int p=JOptionPane.showConfirmDialog(null,"do you really want to delete" ,"Delete",JOptionPane.YES_NO_OPTION);
        if(p==0)
        {
        if(!c_id.getText().equals(""))
        {
            try {
               Connection con = Connect.ConnectDB();
                PreparedStatement ps = con.prepareStatement("DELETE FROM company WHERE c_id = ?");
                int id = Integer.parseInt(c_id.getText());
                ps.setInt(1, id);
                ps.executeUpdate(); 
               update_table();
                JOptionPane.showMessageDialog(null, "company Deleted");
            } catch (SQLException ex) {
          
                JOptionPane.showMessageDialog(null, "company Not Deleted");
            }
           
        }else{
            JOptionPane.showMessageDialog(null, "company Not Deleted : No Id To Delete");
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
            String UpdateQuery;
            PreparedStatement ps;
            Connection con = Connect.ConnectDB();
        try {
                    UpdateQuery = "UPDATE company SET c_name = ?, seats = ? , c_type =? " 
                            + "WHERE c_id = ?";
                    ps = con.prepareStatement(UpdateQuery);
                    String value1=c_type.getSelectedItem().toString();
                       
                    ps.setString(1,c_name.getText());
                    ps.setString(2,seats.getText());
                    ps.setString(3, value1);
                    ps.setString(4,c_id.getText());     
                    ps.executeUpdate();
                 update_table();
                    JOptionPane.showMessageDialog(null, "company Updated");
                    
                } 
                  catch (SQLException ex) 
                  {
                    JOptionPane.showMessageDialog(null, "cannot be  Updated");
                   } 
    }//GEN-LAST:event_updateActionPerformed

